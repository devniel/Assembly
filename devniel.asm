VSync proc 
	pusha
    mov dx,03dah
    WaitNotVSync: ;wait to be out of vertical sync
    in al,dx
    and al,08h
    jnz WaitNotVSync
    WaitVSync: ;wait until vertical sync begins
    in al,dx
    and al,08h
    jz WaitVSync
	popa
    ret
