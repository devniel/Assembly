;================================================ BUFFER A PANTALLA

Buffer_a_Pantalla PROC
    ; Propsito : Copia 64 Kb desde
    ; Entrada   : MAX_BLOQUE, BufSeg
    ; Salida    :
    ; Destruye  : AX, CX, SI, DI
    PUSH      DS
    PUSH      ES

    MOV       AX, 0a000h
    MOV       ES, AX
    MOV       AX, WORD PTR [BufSeg]
    MOV       DS, AX
    XOR       SI, SI
    XOR       DI, DI
    MOV       CX, 64000  

	call VSync
		
	CLD
    REP       MOVSB
    POP       ES
    POP       DS
    RET
Buffer_a_Pantalla ENDP

